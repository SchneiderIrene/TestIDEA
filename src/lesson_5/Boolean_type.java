package lesson_5;

public class Boolean_type {
    public static void main(String[] args) {
          boolean a1 = true;
          boolean b1 = true;
        System.out.println(a1^b1);

        boolean a2 = true;
        boolean b2 = false;
        System.out.println(a2^b2);

        boolean a3 = false;
        boolean b3 = false;
        System.out.println(a3^b3);
    }
}
